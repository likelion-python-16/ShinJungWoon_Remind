# 1. 프로그램이 시작되면 `flatten`이라는 함수를 정의한다.  
#     이 함수는 딕셔너리 형태의 데이터를 평탄화하는 역할을 한다.
    
# 2. `flatten` 함수는 다음과 같은 인자를 받는다:
#     - `data`: 중첩된 딕셔너리 자료
#     - `parent_key`: 재귀적으로 키 이름을 이어붙일 때 사용하는 문자열 (초기값은 빈 문자열)
#     - `sep`: 키를 연결할 때 사용할 구분자 (기본은 `"."`)
        
# 3. 함수 내부에서 결과를 담을 빈 리스트 `items`를 만든다.
    
# 4. 주어진 데이터 `data`의 모든 키-값 쌍에 대해 반복한다.
    
# 5. 각 키-값 쌍에 대해:
    
#     - 새 키를 만든다. 만약 상위 키가 있으면 `"상위키.현재키"`로 만들고, 없으면 그냥 현재 키를 사용한다.
#     - 만약 값이 또 다른 딕셔너리라면:
#         - `flatten` 함수를 재귀적으로 호출해 그 내부 값들도 평탄화한다.
#         - 그 결과를 리스트에 추가한다.
#     - 그렇지 않으면 현재 키와 값을 튜플 형태로 리스트에 추가한다.
        
# 6. 반복이 끝나면 `items` 리스트를 딕셔너리로 바꾸어 반환한다.
	
# 7. 이후, 평탄화할 중첩 데이터를 하나 생성한다.  
#     이 딕셔너리는 예시로 사용자 정보와 위치 정보를 포함하고 있다.
    
# 8. `flatten` 함수를 이용하여 위 데이터를 평탄화한다.
    
# 9. 평탄화된 결과를 `flattened.json`이라는 이름으로 파일에 저장한다.
    
#     - 파일은 UTF-8 인코딩으로 저장되며, 사람이 읽기 쉽게 들여쓰기 형식이 적용된다.
        
# 10. 모든 작업이 끝나면 터미널에 `"데이터가 'flattened.json'에 저장되었습니다."`라는 메시지를 출력한다.
# ```